    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        Account a = (Account) session.getAttribute("account");
        Part part = request.getPart("avatar");
        String realPath = request.getServletContext().getRealPath("/Images/Avatar");
        String filesubmit = part.getSubmittedFileName();
        String generateFileName = UUID.randomUUID().toString().replace("-", "");
        String filename = generateFileName + "-" +  filesubmit;
        part.write(realPath + "/" + filename);
        AccountDAO adao = new AccountDAO();
        adao.changeAvatar(a.getEmail(), "Images/Avatar/" + filename);
        request.setAttribute("avtmsg", "Thay đổi ảnh thành công");
        session.setAttribute("account", adao.getAccountByEmail(a.getEmail()));
        request.getRequestDispatcher("profile.jsp").forward(request, response);
    }

